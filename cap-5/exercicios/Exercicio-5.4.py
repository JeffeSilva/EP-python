#!/usr/bin/env python
# Exercicio 5.4 - Modifique o programa anterior para imprimir de 1 até o número digitado pelo usúario, mas dessa vez, apenas os números ímpares.
print(' ')
fim = int(input('Digite o último número que deseja que seja impresso: '))
print(' ')
x = 1
while x <= fim:
    print(x)
    x = x + 2
print(' ')
print('FIM ')