#!/usr/bin/env python
print (' ')
alt = float(input('Informe a altura da parede: '))
larg = float(input('Informe a largura da parede: '))
print (' ')
area = (alt * larg)
tppap = area / 2
print (f'{alt}m x {larg}m, a área total da parede é {area}m²')
print (f'Para pintar uma área de {area}m², sera necessario {tppap}l de tinta.')
print (' ')
 
